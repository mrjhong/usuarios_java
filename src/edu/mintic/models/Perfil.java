
package edu.mintic.models;

public class Perfil {
    
    private Integer codPerfil;
    private  String nombrePerfil;

    public Perfil() {
        
    }

    public Integer getCodPerfil() {
        return codPerfil;
    }

    public void setCodPerfil(Integer codPer) {
        codPerfil = codPer;
    }

    public String getNombrePerfil() {
        return nombrePerfil;
    }

    public void setNombrePerfil(String nombrePerf) {
        nombrePerfil = nombrePerf;
    }
    
    
  
}
